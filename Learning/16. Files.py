# File I/O is an Operation to perform input and output on files
# Modes in Files
# 1. "r" - Open for Reading (Defalut)
# 2. "w" - Open for Writing , Truncating (Overwrite the data delete old data and write new Data to the file) for file first 
# 3. "x" - Create a new file and open it for writing.
# 4. "a" - Open for writing and appending to the end of the file if exist.
# 5. "b" - Binary Mode
# 6. "t" - Text mode (Defalut) 
# 7. "+" - Open a disk file for updating (reading and writing)


# File mode for use - 
# 1. "r+" - No Truncate but overwrite the file while writing.
# 2. "w+" - Truncate the Entire file.
# 3. "a+" - No Truncate file must be write at the end.